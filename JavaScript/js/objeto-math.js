let numero = 23.1893;
console.log(numero);
/* Arredonda o número fracionário para cima  */
console.log(`Math.ceil(${numero}) = ${Math.ceil(numero)}`);

/* Arredonda o número fracionário para baixo  */
console.log(`Math.floor(${numero}) = ${Math.floor(numero)}`);

/* Arredonda o número fracionário ou para cima se mairo que 49 ou para baixo se menor que 49  */
console.log(`Math.round(${numero}) = ${Math.round(numero)}`);


/* retorna maior valor dentro de um intervalo */
console.log(Math.max(5, 6, 60, 260))

/* retorna menor valor dentro de um intervalo */
console.log(Math.min(5, 6, 60, 260))


/* retorna a parte inteira do  valor*/
console.log(`parseInt(${numero}) = ${parseInt(numero)}`);

/* Eleva a uma potencia */
console.log(Math.pow(8,2));
console.log((8**2));

/* Sorteia um número aleatório */
console.log(Math.random());

/* Raiz quadrada */
console.log(Math.sqrt(25));

/* Raiz cúbica */
console.log(Math.cbrt(27));








